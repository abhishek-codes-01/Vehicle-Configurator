package com.vehicleconfig.vehicle_details_service;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.data.jpa.repository.config.EnableJpaRepositories;
import org.springframework.web.servlet.config.annotation.CorsRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@SpringBootApplication
@ComponentScan(basePackages = "com.vehicleconfig.vehicle_details_service")
@EnableJpaRepositories(basePackages = "com.vehicleconfig.vehicle_details_service.repository")
public class VehicleDetailsServiceApplication {

    public static void main(String[] args) {
        SpringApplication.run(VehicleDetailsServiceApplication.class, args);
        System.out.println("ðŸš— Vehicle Details Microservice started on port 8082");
    }

    @Bean
    public WebMvcConfigurer corsConfigurer() {
        return new WebMvcConfigurer() {
            @Override
            public void addCorsMappings(CorsRegistry registry) {
                registry.addMapping("/**")
                        .allowedOrigins("http://localhost:8081")
                        .allowedMethods("GET", "POST", "PUT", "DELETE", "OPTIONS")
                        .allowedHeaders("*")
                        .allowCredentials(true);
            }
        };
    }
}
